#include <input/Keyboard.h>

namespace baizel
{
	//////////////////////////////////////////////////////////////////////////
	// CONSTRUCTORS
	//////////////////////////////////////////////////////////////////////////

	// -----------------------------------------------------------------------

	iKeyboard::iKeyboard()
	{
		mvPressedKeys.reserve(eKey_Count);
	}

	// -----------------------------------------------------------------------

	//////////////////////////////////////////////////////////////////////////
	// PUBLIC METHODS
	//////////////////////////////////////////////////////////////////////////

	// -----------------------------------------------------------------------

	//////////////////////////////////////////
	// Accessors
	//////////////////////////////////////////

	eKey iKeyboard::GetLastKey() const
	{
		if (mvPressedKeys.empty())
			return eKey_None;

		return mvPressedKeys[0];
	}

	// -----------------------------------------------------------------------
}