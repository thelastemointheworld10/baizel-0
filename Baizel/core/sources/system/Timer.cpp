#include <system/Timer.h>

namespace baizel
{
	//////////////////////////////////////////////////////////////////////////
	// CONSTRUCTORS
	//////////////////////////////////////////////////////////////////////////

	// -----------------------------------------------------------------------

	cTimer::cTimer(iSystem* apSystem)
	{
		mpSystem = apSystem;
	}

	// -----------------------------------------------------------------------

	//////////////////////////////////////////////////////////////////////////
	// PUBLIC METHODS
	//////////////////////////////////////////////////////////////////////////

	// -----------------------------------------------------------------------

	//////////////////////////////////////////
	// Runtime Control
	//////////////////////////////////////////

	void cTimer::Start()
	{
		mlStartTime = mpSystem->GetTimeInMilliSec();
	}

	//////////////////////////////////////////
	// Accessors
	//////////////////////////////////////////

	float cTimer::GetTimeInMilliSec()
	{
		return mpSystem->GetTimeInMilliSec() - mlStartTime;
	}

	float cTimer::GetTimeInSec()
	{
		return (mpSystem->GetTimeInMilliSec() - mlStartTime) / 1000.0f;
	}

	// -----------------------------------------------------------------------
}